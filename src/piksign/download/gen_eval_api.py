"""Generate FRESH eval fakes via the target commercial models.

These images are never used for training - they are the out-of-distribution
test that proves (or disproves) generalization: different prompt distribution,
current model versions, generated by us at eval time.

Providers:
  gemini  -> Nano Banana (model gemini-2.5-flash-image), env GEMINI_API_KEY
  openai  -> GPT-4o image gen (model gpt-image-1),       env OPENAI_API_KEY

Roughly $0.03-0.05 per image; the script prints an estimate and requires --yes.

    python -m piksign.download.gen_eval_api --provider gemini --n 400 --style mixed --yes
    python -m piksign.download.gen_eval_api --provider openai --n 400 --style mixed --yes
"""
from __future__ import annotations

import argparse
import base64
import json
import os
import random
import time
from pathlib import Path

from tqdm import tqdm

from ..paths import ensure, eval_dir

SUBJECTS = [
    "an elderly fisherman", "a golden retriever", "two teenagers", "a street vendor",
    "a cyclist", "a barista", "a wedding couple", "a construction worker",
    "a grandmother knitting", "a chess player", "a violinist", "schoolchildren",
    "a mechanic", "a nurse", "a farmer", "a skateboarder",
]
SETTINGS = [
    "on a rainy city street", "in a cluttered garage", "at a crowded night market",
    "on a foggy pier", "in a sunlit kitchen", "at a rural train station",
    "inside a old library", "on a mountain trail", "in a hospital corridor",
    "at a beach bonfire", "in an office cubicle", "at a farmers market",
]
TIMES = [
    "at golden hour", "under harsh midday sun", "on an overcast afternoon",
    "at dusk with streetlights on", "in early morning mist", "at night with neon signs",
]
CAMERAS = [
    "candid smartphone photo", "35mm film photo with slight grain",
    "casual snapshot with imperfect framing", "photojournalism style",
    "slightly blurry handheld shot", "amateur photo with harsh flash",
]
PHOTO_TEMPLATE = "{camera} of {subject} {setting} {time}, realistic, natural colors"

IMPLAUSIBLE = [
    "a cat with human hands typing on a laptop, photorealistic",
    "an elephant balancing on a bicycle crossing a city street, news photo style",
    "a man watering a tree that grows umbrellas, overcast day, realistic photo",
    "a grandmother walking a pet crocodile in a shopping mall, candid photo",
    "a business meeting where everyone is a garden gnome, office photography",
    "rain falling upward from the ground into clouds over a village, photo",
    "a traffic jam of horses wearing car mirrors and license plates, photo",
    "a chef cooking pasta that is clearly made of rope, kitchen photo",
    "a violin melting over a park bench like liquid, sunny day, photo",
    "a house with staircases on the outside leading nowhere, real estate photo",
    "a dog casting the shadow of a giraffe on a sunny sidewalk, photo",
    "a swimmer doing laps in a pool of autumn leaves, sports photography",
    "an office worker whose reflection in the window shows a knight in armor",
    "a fruit stand selling glass apples with visible clockwork inside, photo",
    "two moons visible over a city skyline at noon, smartphone photo",
]


def build_prompts(n: int, style: str, seed: int) -> list[dict]:
    rng = random.Random(seed)
    prompts: list[dict] = []
    seen: set[str] = set()
    while len(prompts) < n:
        pick_implausible = (style == "surreal") or (style == "mixed" and len(prompts) % 2 == 1)
        if pick_implausible:
            text = rng.choice(IMPLAUSIBLE)
            kind = "surreal"
        else:
            text = PHOTO_TEMPLATE.format(
                camera=rng.choice(CAMERAS), subject=rng.choice(SUBJECTS),
                setting=rng.choice(SETTINGS), time=rng.choice(TIMES),
            )
            kind = "photo"
        key = f"{kind}:{text}"
        if key in seen and style != "surreal":
            continue
        # allow surreal repeats only when the bank is exhausted
        if key in seen and len(seen) < len(IMPLAUSIBLE):
            continue
        seen.add(key)
        prompts.append({"prompt": text, "kind": kind})
    return prompts


def gen_gemini(prompt: str, model: str) -> bytes | None:
    from google import genai
    client = genai.Client()
    resp = client.models.generate_content(model=model, contents=prompt)
    for cand in resp.candidates or []:
        for part in cand.content.parts or []:
            data = getattr(part, "inline_data", None)
            if data and data.data:
                return data.data if isinstance(data.data, bytes) else base64.b64decode(data.data)
    return None


_OPENAI_QUALITY = "medium"  # set from --quality in main()


def gen_openai(prompt: str, model: str) -> bytes | None:
    from openai import OpenAI
    client = OpenAI()
    resp = client.images.generate(
        model=model, prompt=prompt, size="1024x1024", quality=_OPENAI_QUALITY, n=1
    )
    b64 = resp.data[0].b64_json
    return base64.b64decode(b64) if b64 else None


PROVIDERS = {
    # Nano Banana - blocked on API free tier (0 image quota); use
    # piksign.download.eval_picobanana for a free held-out set instead.
    "gemini": {"fn": gen_gemini, "model": "gemini-2.5-flash-image", "env": "GEMINI_API_KEY",
               "source": "nanobanana_fresh", "usd": 0.04},
    # OpenAI image models (2026): gpt-image-2 (flagship), gpt-image-1.5 (default),
    # gpt-image-1 (deprecating), gpt-image-1-mini (cheapest). Override with --model.
    "openai": {"fn": gen_openai, "model": "gpt-image-1.5", "env": "OPENAI_API_KEY",
               "source": "gpt4o_fresh", "usd": 0.04},
}


def main() -> None:
    ap = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--provider", choices=sorted(PROVIDERS), required=True)
    ap.add_argument("--model", default=None, help="override model id (e.g. gpt-image-2)")
    ap.add_argument("--quality", choices=["low", "medium", "high"], default="medium",
                    help="OpenAI image quality; lower = cheaper (stretches a small budget)")
    ap.add_argument("--n", type=int, default=400)
    ap.add_argument("--style", choices=["photo", "surreal", "mixed"], default="mixed")
    ap.add_argument("--seed", type=int, default=7)
    ap.add_argument("--sleep", type=float, default=1.0, help="seconds between requests")
    ap.add_argument("--yes", action="store_true", help="confirm spending")
    args = ap.parse_args()

    p = PROVIDERS[args.provider]
    model = args.model or p["model"]
    global _OPENAI_QUALITY
    _OPENAI_QUALITY = args.quality
    est = args.n * p["usd"]
    print(f"provider={args.provider} model={model} quality={args.quality} "
          f"n={args.n} est. cost ~${est:.0f} (verify actual on your billing dashboard)")
    if not args.yes:
        raise SystemExit("re-run with --yes to confirm spending")
    if not os.environ.get(p["env"]):
        raise SystemExit(f"set {p['env']} first")

    out = ensure(eval_dir("clean", p["source"], "fake"))
    meta_path = out.parent / "prompts.jsonl"
    prompts = build_prompts(args.n, args.style, args.seed)

    done = 0
    with open(meta_path, "a", encoding="utf-8") as meta:
        for i, item in enumerate(tqdm(prompts, desc=f"generate[{args.provider}]")):
            dest = out / f"{i:05d}_{item['kind']}.png"
            if dest.exists():
                done += 1
                continue
            try:
                data = p["fn"](item["prompt"], model)
            except Exception as e:  # noqa: BLE001
                print(f"[error {i}] {e}")
                time.sleep(max(5.0, args.sleep * 5))
                continue
            if not data:
                print(f"[empty {i}] no image returned (safety filter?)")
                continue
            dest.write_bytes(data)
            meta.write(json.dumps({"file": dest.name, "model": model, **item}) + "\n")
            meta.flush()
            done += 1
            time.sleep(args.sleep)
    print(f"generated {done}/{len(prompts)} -> {out}")


if __name__ == "__main__":
    main()
